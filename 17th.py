# ------------------------------------ Python Swapping -------------------------------------------


a = 50
b = 100

# temp = a = 50
temp = a
# a = b = 100
a = b
# b = 50
b = temp

print("A= ",a)
print("B= ",b)


# Swapping shortcut in python
a = 50
b = 100

a, b = b, a
print("A is: ",a)
print("B is: ",b)
